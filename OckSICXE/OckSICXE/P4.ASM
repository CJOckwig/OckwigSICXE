COPY   START   0
.      EXTDEF  BUFFER,BUFEND,LENGTH
.      EXTREF  RDREC,WRREC
COUNT  EQU     25
FIRST  STL     RETADR
CLOOP  +JSUB   RDREC
       LDA     LENGTH
       COMP    #0
       JEQ     ENDFIL
       +JSUB    WRREC
       J       CLOOP
ENDFIL LDA     =C'EOF'
       STA     BUFFER
       LDA     #3
       STA     LENGTH
       +JSUB    WRREC
       J       @RETADR
RETADR RESW    1
LENGTH RESW    1
BUFFER RESB    COUNT+10
BUFEND RESB    1
MAXLEN EQU     BUFEND-BUFFER
       END     FIRST
